    fun getRandomQuote(): String {
        if (currentDeck.isEmpty()) {
            // 덱이 비었으면 다시 채우고 섞음 (무한 리필)
            currentDeck.addAll(allQuotes.shuffled())
        }
        // 카드 한 장 뽑기 (꺼내서 보여주고 덱에서 삭제)
        return currentDeck.removeAt(0)
    }
